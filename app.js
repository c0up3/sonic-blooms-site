const encode = (value) => encodeURIComponent(`${artist} ${value}`);
const spotifyUrl = (title) => `https://open.spotify.com/search/${encode(title)}`;
const youtubeMusicUrl = (title) => `https://music.youtube.com/search?q=${encode(title)}`;
const videoForTrack = (title) => musicVideos.find((video) => video.trackTitle === title || video.title === title);

function platformLinks(track) {
  const links = [
    ["Apple", track.apple, "apple"],
    ["Spotify", spotifyUrl(track.title), "spotify"],
    ["YouTube", youtubeMusicUrl(track.title), "youtube"],
  ];
  const musicVideo = videoForTrack(track.title);
  if (musicVideo) {
    links.push(["Music Video", `https://www.youtube.com/watch?v=${musicVideo.youtube}`, "video"]);
  }
  return links
    .map(
      ([label, url, platform]) =>
        `<a class="platform-link platform-link--${platform}" href="${url}" target="_blank" rel="noreferrer">
          <span class="platform-icon" aria-hidden="true">${platformIcon(platform)}</span>
          <span>${label}</span>
        </a>`,
    )
    .join("");
}

