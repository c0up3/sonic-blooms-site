function openSong(title) {
  const track = allTracks.find((item) => item.title === title);
  if (!track) return;
  const dialog = document.querySelector("#song-dialog");
  document.querySelector("#dialog-content").innerHTML = `
    <div class="dialog-inner">
      <img src="${track.cover}" alt="${track.releaseTitle} cover art" />
      <div>
        <p class="track-era">${track.releaseTitle}</p>
        <h2 id="dialog-title">${track.title}</h2>
        <p>${track.note}</p>
        <div class="play-links">${platformLinks(track)}</div>
      </div>
    </div>
  `;
  dialog.showModal();
}

function wireInteractions() {
  document.querySelector(".dialog-close").addEventListener("click", () => {
    document.querySelector("#song-dialog").close();
  });

  document.querySelectorAll(".oauth-row button").forEach((button) => {
    button.addEventListener("click", () => {
      const message = document.querySelector("#fan-message");
      message.textContent = `${button.dataset.provider} sign-in is coming soon. Use email for now and your Signal Room pass will stay on this device.`;
    });
  });
}

function wireScrollState() {
  const navButtons = [...document.querySelectorAll("#era-nav button")];
  const cards = [...document.querySelectorAll(".release-card[data-era]")];
  const observer = new IntersectionObserver(
    (entries) => {
      const visible = entries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!visible) return;
      const id = visible.target.dataset.era;
      navButtons.forEach((button) => button.classList.toggle("is-active", button.dataset.jump === id));
    },
    { threshold: [0.25, 0.45, 0.65] },
  );
  cards.forEach((card) => observer.observe(card));

  const updateProgress = () => {
    const max = document.documentElement.scrollHeight - window.innerHeight;
    const progress = max > 0 ? window.scrollY / max : 0;
    document.documentElement.style.setProperty("--scroll-progress", Math.min(1, Math.max(0, progress)).toFixed(4));
  };
  updateProgress();
  window.addEventListener("scroll", updateProgress, { passive: true });
  window.addEventListener("resize", updateProgress);
}

function wireFanForm() {
  const form = document.querySelector("#fan-form");
  const message = document.querySelector("#fan-message");
  const saved = localStorage.getItem("sonicBloomsFan");
  if (saved) {
    message.textContent = "Signal Room unlocked on this device. New drops will appear here first.";
  }

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    const payload = Object.fromEntries(new FormData(form));
    message.textContent = "Sending signal...";
    try {
      const response = await fetch("/api/fan-signup", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload),
      });
      const data = await response.json().catch(() => ({}));
      localStorage.setItem("sonicBloomsFan", JSON.stringify({ email: payload.email, date: new Date().toISOString() }));
      message.textContent = data.message || "Signal Room unlocked. Welcome in.";
    } catch (error) {
      localStorage.setItem("sonicBloomsFan", JSON.stringify({ email: payload.email, date: new Date().toISOString() }));
      message.textContent = "Signal Room unlocked on this device. New drops will appear here first.";
    }
  });
}

