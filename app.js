const allTracks = releases.flatMap((release) =>
  release.tracks.map(([title, apple, youtube, note]) => ({
    title,
    apple,
    youtube,
    note,
    releaseId: release.id,
    releaseTitle: release.title,
    cover: release.cover,
  })),
);

const encode = (value) => encodeURIComponent(`${artist} ${value}`);
const spotifyUrl = (title) => `https://open.spotify.com/search/${encode(title)}`;
const youtubeMusicUrl = (title) => `https://music.youtube.com/search?q=${encode(title)}`;
const youtubeUrl = (track) =>
  track.youtube
    ? `https://www.youtube.com/watch?v=${track.youtube}`
    : `https://www.youtube.com/results?search_query=${encode(track.title)}`;

function platformLinks(track) {
  return [
    ["Apple", track.apple],
    ["Spotify", spotifyUrl(track.title)],
    ["YouTube Music", youtubeMusicUrl(track.title)],
    ["YouTube", youtubeUrl(track)],
  ]
    .map(([label, url]) => `<a href="${url}" target="_blank" rel="noreferrer">${label}</a>`)
    .join("");
}

function renderEraNav() {
  const nav = document.querySelector("#era-nav");
  nav.innerHTML = releases
    .map(
      (release, index) =>
        `<button type="button" data-jump="${release.id}" class="${index === 0 ? "is-active" : ""}">
          <span>${release.kicker}</span><strong>${release.title}</strong>
        </button>`,
    )
    .join("");

  nav.addEventListener("click", (event) => {
    const button = event.target.closest("button[data-jump]");
    if (!button) return;
    document.querySelector(`#${button.dataset.jump}`)?.scrollIntoView({ behavior: "smooth", block: "start" });
  });
}

